def get_summ():
    '''Выводит результат сложения двух вводимых чисел'''
    a = int(input('Введите первое число: '))
    b = int(input('Введите второе число: '))
    print(f'Итоговая сумма равна: {a + b}')


def get_difference():
    '''Выводит результат вычитания второго из введенных чисел из первого'''
    a = int(input('Введите первое число: '))
    b = int(input('Введите второе число: '))
    print(f'Итоговая сумма равна: {a - b}')
